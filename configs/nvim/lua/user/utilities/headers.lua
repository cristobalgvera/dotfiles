    add_footer = true,
    add_footer_gap = true,
    footer_offset = 0,
    footer = {
      "          ",
      "    Dani y Cris    ",
      "          ",
    },
  })

  -- INFO: Don't show the NSFW headers during work hours
  local current_hour = tonumber(os.date "%H")
  local current_day = tonumber(os.date "%w")
  local is_working_time = current_day >= MONDAY
    and current_day <= FRIDAY
    and current_hour >= START_WORK_HOUR
    and current_hour < END_WORK_HOUR

  if opts.nsfw and is_working_time then return M.get_random_header() end

  if not opts.add_footer then return header end

  if opts.add_footer_gap then header[#header + 1] = "" end

  local footer_start = math.floor((#header[1] - #opts.footer[1]) / 8) + opts.footer_offset
  for i, line in ipairs(opts.footer) do
    header[#header + 1] = string.rep(" ", footer_start) .. line
  end

  return header
end

